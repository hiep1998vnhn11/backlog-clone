     * @return \Illuminate\Http\Response
     */
    public function store(CreateProjectRequest $request)
    {
        Project::create(
            array_merge($request->validated(), [
                'user_id' => auth()->id(),
            ])
        );
        return $this->sendRespondSuccess($request->key);
    }

